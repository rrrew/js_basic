'use strict'

/**
 * Функция складывает два числа
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */

function add(a, b) {
    return a + b;
}

/**
 * Функция вычитает два числа
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */

function subtract(a, b) {
    return a - b;
}

/**
 * Функция умножает два числа
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */

function multiply(a, b) {
    return a * b;
}

/**
 * Функция делит два числа
 * @param {number} a 
 * @param {number} b 
 * @returns {number}
 */

function divide(a, b) {
    return a / b;
}